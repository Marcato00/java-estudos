class HelloWorld {
	public static void main (String[] args) {
		System.out.println("Hello World!");
	}
}

/* 
Declaração da classe: class
Nome da classe: HelloWorld (sempre inicial com letra maiúscula)
Método: o que está dentro da classe
Modificadores de acesso do método: public static
Tipo de retorno do método: void
Nome do método: main
Parâmetros/Argumentos do método: String[] args
Código: System.out.println("Hello World!");
Blocos estão entre {} (chaves)
*/